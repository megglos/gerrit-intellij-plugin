package com.urswolfer.intellij.plugin.gerrit.rest.bean;

import java.util.Date;

/**
 * @author Urs Wolfer
 */
public class ApprovalInfo extends AccountInfo {
    private int value;
    private Date date;

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    @Override
    public String toString() {
        return "ApprovalInfo{" +
                "value=" + value +
                ", date=" + date +
                "} " + super.toString();
    }
}
